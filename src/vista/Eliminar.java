    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int idLibro = Integer.parseInt(eliminartxt.getText());
        Registro reg = new Registro();
        boolean eliminado = reg.eliminar(idLibro);
        if(eliminado){
            JOptionPane.showMessageDialog(null, "El Paciente ha sido eliminado");
        }else{
            JOptionPane.showMessageDialog(null, "No se ha encontrado un Paciente para eliminar");// TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed
    }
