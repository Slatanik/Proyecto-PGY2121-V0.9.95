    private void btnBuscarDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDActionPerformed
           String nombreLibro = txtbuscarD.getText();
        Registro reg = new Registro();
        Libro libro = reg.buscarPorTitulo(nombreLibro);
        if(libro.getIdLibro() != 0){
            eliminartxt.setText(String.valueOf(libro.getIdLibro()));
        }else{
            JOptionPane.showMessageDialog(null, "Libro no encontrado");
    }//GEN-LAST:event_btnBuscarDActionPerformed
    }
